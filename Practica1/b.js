// Se define la función 'cuadrado' usando la sintaxis de arrow function.
const cuadrado = (numero) => numero * numero;

// Se prueba la función con 3 números diferentes y se muestra en consola.
console.log(`El cuadrado de 2 es: ${cuadrado(2)}`); // Resultado: 4
console.log(`El cuadrado de 5 es: ${cuadrado(5)}`); // Resultado: 25
console.log(`El cuadrado de 10 es: ${cuadrado(10)}`); // Resultado: 100